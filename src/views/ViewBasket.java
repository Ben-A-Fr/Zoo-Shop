    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
        
        int selectedRow = tblBasket.getSelectedRow();//Retrieve user selection
        
        if(selectedRow == -1)//Nothing selected
        {
            JOptionPane.showMessageDialog
            (null, "Please select the product you wish to delete.");
        }
        
        else//Something IS selected
        {
            
            int productId = Integer.parseInt(String.valueOf(tblBasket.getValueAt(selectedRow, 0)));
            
            currentOrder.removeOrderLine(productId);//Remove from db
            
            JOptionPane.showMessageDialog
            (null, "Product removed from basket successfully!");//Confirmation msg
            
            //Remove from table
            DefaultTableModel basketModel = (DefaultTableModel)tblBasket.getModel();
            basketModel.removeRow(selectedRow);
            tblBasket.setModel(basketModel);
            
            lblTotal.setText(String.format("%.02f", currentOrder.getOrderTotal()));//Update total cost
        }
        
    }//GEN-LAST:event_btnRemoveProductActionPerformed

    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        
        if(currentOrder.getOrderLines().isEmpty())//Basket is empty
        {
            JOptionPane.showMessageDialog(null, "Your basket is currently empty.");
        }
        
        else
        {
            
            boolean canBuy = true;//Initialise canBuy bool
            
            for(Map.Entry<Integer, OrderLine> olEntry : currentOrder.getOrderLines().entrySet())//For each product in basket...
            {
                OrderLine ol = olEntry.getValue();
                
                //Check the stock level for each product
                if((ol.getProduct().getStockLevel()) < ol.getQuantity())//Product is out of stock
                {
                    canBuy = false;
                    JOptionPane.showMessageDialog
                    (null, "The product " + ol.getProduct().getProductId() + " is out of stock!");                   
                }
            }
            
            if(canBuy)//Runs if all products found in stock
            {
                currentOrder.completeOrder();//Label order as complete in db
                
                //Navigate to the Order Confirmation view
                OrderConfirmation confirmation = new OrderConfirmation(loggedInCustomer, currentOrder);
                this.dispose();
                confirmation.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnPurchaseActionPerformed

