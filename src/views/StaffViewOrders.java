    private void btnViewOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrderActionPerformed
        
        int selectedRow = tblPreviousOrders.getSelectedRow();//Retieve selection
        
        if(selectedRow == -1)//i.e. nothing selected
        {
            JOptionPane.showMessageDialog(null, "Please select an order to view.");
        }
        
        else
        {
            //Retrieve order data
            String customerUsername = String.valueOf(tblPreviousOrders.getValueAt(selectedRow, 0));
            int orderId = Integer.parseInt(String.valueOf(tblPreviousOrders.getValueAt(selectedRow, 1)));
            
            //Navigate to the Staff View Order Lines view passing order dta to be displayed
            StaffViewOrderLines previousOrderLines = new StaffViewOrderLines(loggedInStaff, customerUsername, orderId);
            this.dispose();
            previousOrderLines.setVisible(true);          
        }
        
    }//GEN-LAST:event_btnViewOrderActionPerformed

