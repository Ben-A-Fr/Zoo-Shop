    public StaffViewOrders(Staff staff)
    {
        loggedInStaff = staff;
        
        initComponents();//Load view
        
        DbManager db = new DbManager();
        customers = db.loadCustomers();//Load all customers onto hash map
        
        DefaultTableModel ordersModel = (DefaultTableModel)tblPreviousOrders.getModel();
        
        for(Map.Entry<String, Customer> custEntry : customers.entrySet())
        {
            Customer cust = custEntry.getValue();
            for(Map.Entry<Integer, Order> oEntry : cust.getOrders().entrySet())
            {
                Order order = oEntry.getValue();

                if(order.getStatus().equals("Complete"))
                {
                    ordersModel.addRow(new Object[]//Display order details of all customers
                    {cust.getUsername(),
                        order.getOrderId(),
                        order.getOrderDate(),
                        "Â£" + String.format("%.02f",order.getOrderTotal())});
                }           
            }
        }
    }

