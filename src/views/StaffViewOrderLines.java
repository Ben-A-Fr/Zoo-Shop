    public StaffViewOrderLines(Staff staff, String custUsername, int oId)
    {
        loggedInStaff = staff;
        customerUsername = custUsername;
        orderId = oId;
        
        initComponents();//Load view
        
        DbManager db = new DbManager();
        HashMap<String, Customer> customers = db.loadCustomers();//Load all customers onto a hash map
        
        Customer customerToView = customers.get(customerUsername);
        Order orderToView = customerToView.getOrders().get(orderId);
                 
        DefaultTableModel orderLinesModel = (DefaultTableModel)tblPreviousOrderLines.getModel();
             
        for(Map.Entry<Integer, OrderLine> olEntry : orderToView.getOrderLines().entrySet())
        {
            OrderLine orderLine = olEntry.getValue();
            Product product = orderLine.getProduct();
            
            orderLinesModel.addRow(new Object[]//Load customer order details onto the table
            {product.getProductId(),
                product,
                "Â£" + String.format("%.02f", orderLine.getLineTotal()),
                orderLine.getQuantity()});
        }
    }

